package br.com.fiap.model.vo;

public class StatVO {
	private String name;

	public StatVO(String name) {
		this.name = name;
	}
	
	public StatVO() {
		
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	
}
